public class contador {
    
    public static int somar(int n1, int n2) {
        int sum;
        sum = n1+n2;
        return sum;
    }

    public static int subtrair(int n1, int n2) {
        int sum;
        sum = n1-n2;
        return sum;
    }

    public static int multiplicar(int n1, int n2) {
        int sum;
        sum = n1*n2;
        return sum;
    }

    public static int dividir(int n1, int n2) {
        int sum;
        sum = n1/n2;
        return sum;
    }









}
